// 3) Создайте массив объектов с описанием пользователей(такой мы делали в уроке). Объявите отдельную переменную, в которой будет храниться количество простых пользователей, начальное значение будет - 0. Обойдите массив пользователей и если пользователь не является админом - прибавьте к ранее созданной переменной единицу. После окончания работы цикла выведите в консоль переменную с количеством простых пользователей.
// Создаем массив объектов с описанием пользователей
const users = [
    { name: 'Alice', age: 25, role: 'user' },
    { name: 'Bob', age: 30, role: 'admin' },
    { name: 'Charlie', age: 22, role: 'user' },
    { name: 'David', age: 28, role: 'user' },
    { name: 'Eve', age: 35, role: 'admin' }
];
// Объявляем переменную для хранения количества простых пользователей
let simpleUserCount = 0;
// Перебираем массив пользователей
for (let i = 0; i < users.length; i++) {
    if (users[i].role !== 'admin') {
        simpleUserCount++; // Увеличиваем счетчик, если пользователь не админ
    }
}
// Выводим количество простых пользователей в консоль
console.log(simpleUserCount); // Вывод: 3
